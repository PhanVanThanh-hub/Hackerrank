def jumpingOnClouds(c):
    i=0
    temp=0
    while i<len(c)-2:
        if c[i+2]!=1:
            temp+=1
            i+=2
        else:
            temp+=1
            i+=1
    if len(c)%2==0:
        if len(c)-i==2:
            temp+=1
    return temp
